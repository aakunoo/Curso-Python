''' Sintaxis: for (variable) in (elemento a recorrer)
El elemento a recorrer puede ser bian diccionario, listas, cadenas de texto... etc.
'''

# La variable i se encarga de contar cuantos elementos hay en este caso, en la lista.
for meses_agno in ["Enero", "Febrero", "Marzo", "Abril", "Mayo", "Junio", "Julio", "Agosto", "Septiembre", "Octubre", "Noviembre", "Diciembre"]:
    print(meses_agno)

